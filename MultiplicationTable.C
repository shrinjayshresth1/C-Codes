#include<stdio.h>
int main()
{
    /*Multiplication table of a number entered by the user 
    */
   int num;
   printf("Enter the number:-");
   scanf("%d",&num);
   printf(" %d x 1 = %d\n",num,num*1);
   printf(" %d x 2 = %d\n", num, num * 2);
   printf(" %d x 3 = %d\n", num, num * 3);


   return 0;
} 